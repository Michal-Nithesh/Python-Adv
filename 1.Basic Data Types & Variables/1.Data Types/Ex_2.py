# Takes two integers as input and prints their sum, difference, product, and quotient

# Input
a = int(input("Enter the value a:"))
b = int(input("Enter the value b:"))

# Sum
sum = a + b
print("Sum of two number is:", sum)
print("Type of sum is:", type(sum))

# Difference
dif = a - b
print("Dif of two number is:", dif)
print("Type of dif is:", type(dif))

#Product
multi = a * b
print("Multiple of two number is:", multi)
print("Type of multi is:", type(multi))

# Quotient
quo = a / b
print("Quo of two number is:", quo)
print("Type of quo is:", type(quo))